int	ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[1] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return ((unsigned int)s1[i] - (unsigned int)s2[i]);
		}
		i++;
	}
	return ((unsigned int)s1[i] - (unsigned int)s2[i]);
}
