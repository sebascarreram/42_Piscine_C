int	ft_iterative_factorial(int nb)
{
	int a;

	if (nb > 0 && nb < 13)
	{
		a = nb;
		nb--;
		while (nb > 1)
		{
			a = a * nb;
			nb--;
		}
	}
	else
	{
		return (0);
	}
	return (a);
}
